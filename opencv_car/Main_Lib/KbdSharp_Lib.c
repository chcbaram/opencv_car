//----------------------------------------------------------------------------------------------
//            TITLE   : KbdSharp_Init( void )
//
//            WORK    : 
//            
//            DATE    : 2004. 3. 19
//----------------------------------------------------------------------------------------------
U16 KbdSharp_Init( void )
{

	KbdSharp_Handle = open( KBD_SHARP_DRV, O_RDWR );
	
	if ( KbdSharp_Handle < 0 ) 
	{
		printf( "Error opening %s device !\n", KBD_SHARP_DRV );
		exit( 1 );
	}			
}




//----------------------------------------------------------------------------------------------
//            TITLE   : KbdSharp_Putch
//
//            WORK    : 
//            
//            DATE    : 2004. 3. 19
//----------------------------------------------------------------------------------------------
U16 KbdSharp_Putch( long Key )
{
	ioctl( KbdSharp_Handle, SHARP_KBDCTL_SENDKEY, Key );
}





//----------------------------------------------------------------------------------------------
//            TITLE   : KbdSharp_Close
//
//            WORK    : 
//            
//            DATE    : 2004. 3. 19
//----------------------------------------------------------------------------------------------
void KbdSharp_Close( void )
{
	close( KbdSharp_Handle );
}












